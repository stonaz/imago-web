			<h2 class="title">Introduzione</h2>
	<h4 class="title"><i><b>di Luisa Falchi</b></i></h4>
			 <p>Il censimento dei fondi urbani 
              della città di Roma nasce, in prima istanza, come strumento catastale 
              finalizzato all'imposizione di una tassa per la manutenzione delle 
              strade; la tassazione sarebbe stata ripartita su tutti gli immobili 
              compresi nelle mura, in ragione del loro valore. Viene infatti promosso 
              con l'articolo 38 del motu-proprio del 10 dicembre 1818 sulla manutenzione 
              delle strade interne, che riorganizza l'intero sistema in base al 
              quale si garantiva la viabilità della città ed il decoro urbano. 
              Gli interventi di manutenzione, fino ad a quel momento a carico 
              dei proprietari "frontisti" tra i quali le spese erano ripartite 
              in ragione dell'estensione del fronte-strada di competenza di ciascuna 
              proprietà, saranno da allora affidati direttamente alla Presidenza 
              delle Strade, cui è destinato l'introito della nuova tassa.<br>
              Benchè, con l'introduzione della dativa reale nel 1801, anche Roma 
              fosse chiamata al pagamento di un'imposta diretta sui beni immobili, 
              sembra che anche l'esigenza di una pianta catastale della città, 
              elevata "uniformandosi al Regolamento dei 22 febbraio 1817" sulla 
              misura e formazione delle mappe di tutto lo Stato, fosse avvertita 
              più in funzione della tassa delle strade che in rapporto al prelievo 
              della dativa stessa. Comunque, anche per Roma si pass&ograve; da 
              un catasto descrittivo fatto sulla base delle denunce giurate dei 
              possessori, quale il cosiddetto catasto daziale, ad un catasto geometrico 
              particellare elevato a cura del governo. <br>
              Per la realizzazione dell'opera si ricorse, come per il resto dello 
              Stato, allo strumento dell'appalto, ed il 24 novembre 1818, per 
              gli atti del notaio Giuseppe Mauri segretario e cancelliere della 
              R.C.A., si stipulò il contratto con gli architetti Gaspare Salvi 
              e Giacomo Palazzi, accademici di S. Luca. <br>
              Tra le due scelte, quella di provvedere ad una integrale rilevazione 
              dei fabbricati ex novo e quella di avvalersi della pianta del Nolli 
              (1748), verificandola a correggendola laddove necessario, si opt&ograve;, 
              come narrato dal contratto stesso, per la seconda: gli "architetti 
              intraprendenti" partirono quindi dalla pianta del Nolli che aggiornarono 
              con le variazioni intervenute tra il 1748 ed il 1819 ed emendarono 
              degli eventuali errori. <br>
              Il capitolato prevedeva, inoltre, che la nuova pianta fosse costitutita 
              di tante mappe quante erano i rioni; che tali mappe fossero elevate 
              alla scala 1:1000; che i singoli isolati ("cassoni" o "isole"), 
              a cui si limitava la rilevazione del Nolli, fossero suddivisi nei 
              vari fabbricati e pertinenze di cui si componevano, in modo da "servire 
              agli usi catastali". <br>
              Sulla scorta del Nolli quindi, si elaborò alla scala di 1:1000 quel 
              <i>Rilievo originale a cassoni della città di Roma</i> eseguito 
              nell'anno 1819 e 20, diviso in 9 fogli, che è citato da un inventario 
              tardo conservato nell'archivio della Presidenza del Censo e che 
              non ci è pervenuto. La fase successiva delle operazioni consistette 
              nella stesura delle piante delle <i>Suddivisioni originali</i> di 
              ciascun rione, nelle quali ogni isola, identificata dal suo numero, 
              è appunto suddivisa nei fabbricati e nelle aree di cui è composta, 
              con l'individuazione grafica dei rapporti di pertinenza che legano 
              gli uni alle altre. <br>
              Sulla base di tale materiale, mentre si avviava la campagna di stima 
              dei beni con l'accertamento della loro consistenza, dell'uso a cui 
              erano destinati, dell'ammontare della rendita reale o &quot;reperibile&quot; 
              di cui erano capaci, si passò alla redazione delle mappe definitive 
              dei Rioni, alla numerazione delle particelle, e poi alla stesura 
              della serie dei <i>brogliardi originali</i> di ogni mappa: registri 
              descrittivi che recano a fianco di ciascuna particella l'intestazione 
              del possessore, la localizzazione dell'immobile, la sua consistenza 
              e superficie. <br>
              Bench&eacute; il contratto prevedesse la consegna della nuova pianta 
              di Roma entro sei mesi, la complessità dell'operazione, ma ancor 
              più gli ostacoli incontrati per accedere agli innumerevoli conventi 
              presenti nella città, specie di clausura, ed agli edifici delle 
              rappresentanze straniere, allungarono i tempi di esecuzione. Ultimate 
              quindi le operazioni di stima, regolate dal motu proprio citato 
              e dalle <i>Istruzioni ai periti estimatori delle fabbriche di Roma</i> 
              del 22 febbraio 1819, e dopo l'evasione di ricorsi presentati a 
              seguito della pubblicazione di mappe ed estimi, il nuovo catasto 
              fu quindi attivato a partire da 1824 con editto del Segretario di 
              Stato del 4 ottobre 1824. <br>
              <br>
              <b>Illustrazione delle serie </b><br>
              <b>Piante dei rioni</b> - Si tratta delle mappe originali dei rioni, 
              alcune in un unico foglio, altre in più fogli numerati. I cortili 
              e le pertinenze dei singoli fabbricati sono collegati al rispettivo 
              fabbricato con un tratto ondulato e sono privi di un proprio numero 
              di particella. I monumenti e i ruderi antichi sono tinteggiati in 
              nero, gli edifici in rosa, i corsi d'acqua in celeste. Sono presenti 
              le denominazioni di strade, vicoli, piazze e delle porte della città. 
              In margine alle mappe sono indicate le denominazioni dei rioni confinanti. 
              <br>
              <b>Suddivisioni originali</b> - Si tratta delle piante su cui si 
              identificarono le suddivisioni interne a ciascuna isola, preliminarmente 
              alla stesura delle mappe definitive. Anch'esse suddivise per rione, 
              presentano frequentemente un'identificazione delle particelle tramite 
              lettera alfabetica, in successione interna a ciascuna isola, con 
              richiamo alle rispettive pertinenze: solo in un secondo momento 
              sono stati aggiunti i numeri di particella, attribuiti seguendo 
              un numerazione progressiva unica per rione. Ogni pianta di tale 
              serie è costituita da &quot;porzioni&quot; numerate che recano l'indicazione 
              del geometra disegnatore, appartenente al gruppo dei geometri che 
              operarono alle dipendenze degli architetti appaltatori. Le singole 
              porzioni non coincidono, quanto al territorio rappresentato, con 
              i fogli delle mappe originali che recano lo stesso numero e, nel 
              loro insieme, non coprono l'intero territorio del rione, essendone 
              spesso escluse le aree non edificate. Riutilizzate in epoca postunitaria, 
              come evidenziato da frequenti annotazioni a lapis, riportano l'indicazione 
              dei numeri civici. <br>
              <b><i>Allegati alla Pianta originale della città di Roma aggiornati 
              a tutto il 1871</i></b> - Si tratta, appunto, di piante allegate 
              alle mappe dei singoli rioni, anch'esse costituite da Porzioni numerate 
              per rione, redatte da geometri che non appartengono al gruppo che 
              elevò la Pianta di Roma. Elaborate probabilmente in occasione di 
              una generale revisione che portò, tra l'altro, alla redazione nel 
              1868 della nuova serie dei registri ordinati per intestatario (Cancelleria 
              del Censo di Roma, serie II), furono poi aggiornate a tutto il 1871. 
              Come le porzioni delle Suddivisioni non corrispondono, quanto all'area 
              in esse compresa, con i fogli di mappa recanti lo stesso numero 
              e non coprono nel loro insieme l'intero territorio del rione. Presentano 
              anch'esse l'indicazione dei numeri civici, che è assente dalle mappe 
              originali. Le nuove edificazioni sono rappresentate in rosso così 
              come rossi sono i tratti di correzione, mentre in giallo sono raffigurati 
              gli abbattimenti di corpi di fabbrica preesistenti. <br>
              <b>Brogliardi originali - I serie</b> - Sono i registri descrittivi 
              della particelle presenti sulle relative mappe. Uno per ciascun 
              rione, riportano il numero identificativo della particella o mappale 
              (con eventuali numeri subalterni in caso di fabbricati suddivisi 
              in distinte unità), l'ubicazione del fondo con via e numero civico, 
              la tipologia dell'immobile, l'intestazione del possessore, la consistenza 
              del bene e la sua superficie indicata in tavole (1000mq) e centesimi 
              (10mq). Le particelle sono accorpate per isole, ciascuna delle quali 
              è identificata da un proprio numero. <br>
              <b>Brogliardi - II serie</b> - Si tratta della copia dei brogliardi 
              originali che gli appaltatori erano tenuti a fornire secondo le 
              clausole contrattuali. Non presentano la denominazione di "Brogliardo" 
              ma recano sul frontespizio <i>Censimento generale per le fabbriche 
              della città di Roma</i> seguìto dal nome del Rione. Ai dati presenti 
              nel brogliardo originale, spesso organizzati in maniera più ordinata 
              ed analitica e talvolta corretti rispetto ai precedenti, si aggiungono 
              dati relativi alla stima e cioè l'ammontare della pigione "attuale" 
              o "reperibile" e quello dell'estimo, calcolato in ragione di 100 
              scudi per ogni 8 di reddito annuo. Destinati, come nel caso delle 
              copie dei brogliardi catastali del resto dello Stato, alla Cancelleria 
              del Censo competente per territorio al fine della conservazione 
              dei dati catastali aggiornati, rimasero nella stessa sede della 
              Presidenza Generale del Censo in quanto le funzioni di Cancelliere 
              del Censo di Roma erano ricoperte dallo stesso Direttore Generale 
              del Censo. <br>
              <b>Aggiornamenti</b> - Sono registri che appartengono all'archivio 
              della Cancelleria del Censo di Roma poi Agenzia delle Imposte, serie 
              III. Sono sostanzialmente brogliardi aggiornati al 1871 nel quadro 
              delle operazioni di "rettifica catastale delle proprietà costrutte 
              nella Provincia di Roma", volute dal nuovo Regno d'Italia per uniformare 
              i catasti dei fabbricati e la relativa imposta. Suddivisi per rione, 
              presentano su pagine affrontate la situazione delle proprietà secondo 
              la revisione del 1868 e quella risultante dai nuovi rilievi: la 
              disposizione dei dati è quella tipica dei brogliardi già illustrati 
              e segue quindi l'ordine delle particelle isola per isola. Una finca 
              finale di annotazioni dà conto delle modifiche verificatesi o di 
              accorpamenti di particelle. Costituiscono in parte l'apparato descrittivo 
              di corredo agli <i>Allegati alla Pianta originale della città di 
              Roma aggiornati a tutto il 1871</i>. </p>
